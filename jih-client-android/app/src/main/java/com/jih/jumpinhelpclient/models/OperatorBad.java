package com.jih.jumpinhelpclient.models;

public class OperatorBad {
    public String id_operator;
    public String id_country;
    public String name;
    public String phone_number;
    public String email;

    public String getId_operator() {
        return id_operator;
    }

    public void setId_operator(String id_operator) {
        this.id_operator = id_operator;
    }

    public String getId_country() {
        return id_country;
    }

    public void setId_country(String id_country) {
        this.id_country = id_country;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
